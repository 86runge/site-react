/**
 * 常用的正则匹配
 */
export default {
    // 帐号是否合法(字母开头，允许1-30字节，允许字母数字下划线)
    reg_name: /^[a-zA-Z][a-zA-Z0-9_]{0,29}$/,
    // 简单密码(长度在6~20之间，只能包含字母、数字和下划线)
    reg_password: /^[a-zA-Z0-9_]{6,30}$/,
    // 中文
    reg_cn: /[\u4e00-\u9fa5]/,
    // 手机号码
    reg_mobile: /^1([345678])\d{9}$/,
    // 固定电话、手机号码
    reg_phone: /^0(\d{2}-{0, 1}\d{8})|(\d{3}-{0, 1}\d{7})$/ || /^1([345678])\d{9}$/
}
